// Não podemos criar constantes com plavras reservadas
// Exemplos de palavras reservadas: if, else, for, while, do, break, true, false, function, return, var, let, const
// Constantes precisam ter nomes significativos
// Não podem conter espaços ou traços
// Utilizamos camelCase para nomear nossas constantes
// case sensitive - letras maiúsculas e minúsculas fazem diferença
// Não podemos redeclarar uma constante
// Não podemos alterar o valor de uma constante
// Não utilize var, utilize const (ou let)
// + - * /
// String = Texto "Assim" 'Assim' `Assim`
// Number = 1, 2, 3, 4, 5...
// Boolean = true, false

const primeiroNumero = 5;  //Number tipo de dado
const segundoNumero = 10;  
const resultado = primeiroNumero * segundoNumero;
const resultadoDuplicado = resultado * 2;
let resultadoTriplicado = resultado * 3;
resultadoTriplicado = resultadoTriplicado + 5;

console.log(typeof primeiroNumero);
console.log(primeiroNumero + segundoNumero);
console.log(resultado);
console.log(resultadoDuplicado);
console.log(resultadoTriplicado);
console.log(typeof resultadoTriplicado);

